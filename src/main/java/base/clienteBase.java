package Base;

public class clienteBase {
    private int p_idCliente;
    private int p_Codigo;
    private String p_Organiza;
    private String p_Celular;

    public int getP_idCliente() {
        return p_idCliente;
    }

    public void setP_idCliente(int p_idCliente) {
        this.p_idCliente = p_idCliente;
    }

    public int getP_Codigo() {
        return p_Codigo;
    }

    public void setP_Codigo(int p_Codigo) {
        this.p_Codigo = p_Codigo;
    }

    public String getP_Organiza() {
        return p_Organiza;
    }

    public void setP_Organiza(String p_Organiza) {
        this.p_Organiza = p_Organiza;
    }

    public String getP_Celular() {
        return p_Celular;
    }

    public void setP_Celular(String p_Celular) {
        this.p_Celular = p_Celular;
    }
}
